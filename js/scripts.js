// gallons to liters
var userGallons = parseInt(prompt("Enter number of gallons to convert: "));
var calcToLiters = (userGallons * 3.785411784);
alert(calcToLiters);

// pounds to kg
var userPounds = parseInt(prompt("Enter number of pounds to convert: "));
var calcToKG = (userPounds * 0.45);
alert(calcToKG);

//cups to liters
var userCups = parseInt(prompt("Enter number of cups to convert: "));
var calcCupToLiters = (userCups * .236588);
alert(calcCupToLiters);
